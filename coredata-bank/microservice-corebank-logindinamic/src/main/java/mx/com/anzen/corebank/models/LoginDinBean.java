package mx.com.anzen.corebank.models;

public class LoginDinBean {
	
	private String cliente;
	private String usuario;
	private String idSesion;
	private String valorDinamico;
	
	
	public String getCliente() {
		return cliente;
	}
	public void setCliente(String cliente) {
		this.cliente = cliente;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getIdSesion() {
		return idSesion;
	}
	public void setIdSesion(String idSesion) {
		this.idSesion = idSesion;
	}
	public String getValorDinamico() {
		return valorDinamico;
	}
	public void setValorDinamico(String valorDinamico) {
		this.valorDinamico = valorDinamico;
	}
	
	
	

}
